        // Live Event Countdown Example
        function updateEventCountdown() {
            const events = document.querySelectorAll('.event-date-small');
            events.forEach(event => {
                // This is a simplified example - you would calculate actual countdown
                const daysElement = event.querySelector('.fw-bold');
                const days = parseInt(daysElement.textContent);
                const today = new Date().getDate();
                
                if (days > today) {
                    event.style.opacity = '1';
                } else {
                    event.style.opacity = '0.6';
                }
            });
        }

        // Initialize when page loads
        document.addEventListener('DOMContentLoaded', function() {
            updateEventCountdown();
            
            // Add animation to stats
            const stats = document.querySelectorAll('.stat-item h3');
            stats.forEach(stat => {
                const target = parseInt(stat.textContent);
                let current = 0;
                const increment = target / 50;
                const timer = setInterval(() => {
                    current += increment;
                    if (current >= target) {
                        stat.textContent = target.toLocaleString() + '+';
                        clearInterval(timer);
                    } else {
                        stat.textContent = Math.floor(current).toLocaleString();
                    }
                }, 50);
            });
        });
